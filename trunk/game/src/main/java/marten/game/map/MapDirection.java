package marten.game.map;

/**Defines all possible map directions.
 * @author carnifex*/
public enum MapDirection {
    /**Direction towards the top edge of the map*/
    NORTH,
    /**Direction towards the top left corner of the map*/
    NORTHWEST,
    /**Direction towards the bottom left corner of the map*/
    SOUTHWEST,
    /**Direction towards the bottom edge of the map*/
    SOUTH,
    /**Direction towards the bottom right corner of the map*/
    SOUTHEAST,
    /**Direction towards the top right corner of the map*/
    NORTHEAST
}
